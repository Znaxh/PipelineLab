"""
Text chunking utilities.
Generated by PipelineLab.

Supported methods: fixed, recursive, sentence, paragraph
"""
import re


def chunk(
    text: str,
    method: str = "fixed",
    chunk_size: int = 512,
    overlap: int = 50,
) -> list[str]:
    """
    Split *text* into chunks.

    Parameters
    ----------
    text : str
        The raw text to chunk.
    method : str
        One of "fixed", "recursive", "sentence", "paragraph".
    chunk_size : int
        Target chunk size in characters.
    overlap : int
        Number of overlapping characters between consecutive chunks.
    """
    dispatch = {{
        "fixed": _fixed_chunk,
        "recursive": _recursive_chunk,
        "sentence": _sentence_chunk,
        "paragraph": _paragraph_chunk,
    }}
    fn = dispatch.get(method)
    if fn is None:
        raise ValueError(f"Unknown chunking method: {{method}}")
    return fn(text, chunk_size, overlap)


# ----- Fixed-size chunking -----

def _fixed_chunk(text: str, size: int, overlap: int) -> list[str]:
    """Simple character-level sliding window."""
    chunks: list[str] = []
    start = 0
    while start < len(text):
        end = start + size
        chunks.append(text[start:end])
        start += size - overlap
    return chunks


# ----- Recursive chunking -----

_SEPARATORS = ["\\n\\n", "\\n", ". ", " ", ""]


def _recursive_chunk(text: str, size: int, overlap: int) -> list[str]:
    """Recursively split on decreasingly fine separators."""
    return _split_recursive(text, _SEPARATORS, size, overlap)


def _split_recursive(
    text: str, separators: list[str], size: int, overlap: int
) -> list[str]:
    if len(text) <= size:
        return [text] if text.strip() else []

    sep = separators[0] if separators else ""
    if sep == "":
        return _fixed_chunk(text, size, overlap)

    parts = text.split(sep)
    chunks: list[str] = []
    current = ""
    for part in parts:
        candidate = current + sep + part if current else part
        if len(candidate) > size and current:
            chunks.append(current)
            # Keep overlap from the end of the previous chunk
            current = current[-overlap:] + sep + part if overlap else part
        else:
            current = candidate
    if current.strip():
        chunks.append(current)

    # If any chunk is still too large, split it with the next separator
    if len(separators) > 1:
        refined: list[str] = []
        for c in chunks:
            if len(c) > size:
                refined.extend(_split_recursive(c, separators[1:], size, overlap))
            else:
                refined.append(c)
        return refined
    return chunks


# ----- Sentence-based chunking -----

_SENTENCE_RE = re.compile(r"(?<=[.!?])\\s+")


def _sentence_chunk(text: str, size: int, overlap: int) -> list[str]:
    """Group sentences into chunks up to *size* characters."""
    sentences = _SENTENCE_RE.split(text)
    chunks: list[str] = []
    current = ""
    for sent in sentences:
        if len(current) + len(sent) + 1 > size and current:
            chunks.append(current.strip())
            # Overlap: keep the last *overlap* characters of the previous chunk
            current = current[-overlap:] + " " + sent if overlap else sent
        else:
            current = current + " " + sent if current else sent
    if current.strip():
        chunks.append(current.strip())
    return chunks


# ----- Paragraph chunking -----

def _paragraph_chunk(text: str, size: int, overlap: int) -> list[str]:
    """Split on double-newlines (paragraphs), merging short ones."""
    paragraphs = [p.strip() for p in text.split("\\n\\n") if p.strip()]
    chunks: list[str] = []
    current = ""
    for para in paragraphs:
        if len(current) + len(para) + 2 > size and current:
            chunks.append(current)
            current = current[-overlap:] + "\\n\\n" + para if overlap else para
        else:
            current = current + "\\n\\n" + para if current else para
    if current.strip():
        chunks.append(current)
    return chunks
