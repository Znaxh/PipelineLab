"""
Strategy Knowledge Base
Comprehensive information about every chunking, retrieval, and reranking strategy
available in PipelineLab. Powers the info (i) buttons on pipeline nodes.
"""
from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class StrategyInfo:
    id: str
    name: str
    category: str  # "chunking", "retrieval", "reranking"
    summary: str  # 1 sentence
    when_to_use: list[str]
    when_not_to_use: list[str]
    best_for: list[str]  # document types / use cases
    complexity: str  # "simple", "moderate", "advanced"
    latency: str  # "fast", "moderate", "slow"
    cost: str  # "free", "low", "moderate", "high"
    requires_llm: bool
    requires_gpu: bool
    accuracy_tier: str  # "baseline", "good", "excellent", "state-of-art"
    pairs_well_with: list[str]  # IDs of complementary strategies
    example_config: dict
    decision_factors: list[str]
    tradeoffs: str  # 1-2 sentence tradeoff summary
    pro_tip: str


