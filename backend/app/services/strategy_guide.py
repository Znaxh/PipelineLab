# ---------------------------------------------------------------------------
# StrategyGuide public API
# ---------------------------------------------------------------------------

class StrategyGuide:
    """Central registry and query interface for all strategy information."""

    def __init__(self) -> None:
        self._strategies: dict[str, StrategyInfo] = {}
        for s in _CHUNKING_STRATEGIES + _RETRIEVAL_STRATEGIES + _RERANKING_STRATEGIES:
            self._strategies[s.id] = s

    # -- queries --

    def all_strategies(self) -> list[StrategyInfo]:
        """Return every registered strategy."""
        return list(self._strategies.values())

    def get(self, strategy_id: str) -> StrategyInfo | None:
        """Look up a single strategy by ID."""
        return self._strategies.get(strategy_id)

    def by_category(self, category: str) -> list[StrategyInfo]:
        """Return all strategies in a given category."""
        return [s for s in self._strategies.values() if s.category == category]

    def get_pairs(self, strategy_id: str) -> list[StrategyInfo]:
        """Return strategies that pair well with the given one."""
        s = self._strategies.get(strategy_id)
        if s is None:
            return []
        return [
            self._strategies[pid]
            for pid in s.pairs_well_with
            if pid in self._strategies
        ]

    def compare(self, ids: list[str]) -> list[StrategyInfo]:
        """Return the full info for a list of strategy IDs (for side-by-side comparison)."""
        result = []
        for sid in ids:
            info = self._strategies.get(sid)
            if info is not None:
                result.append(info)
        return result


# Module-level singleton
strategy_guide = StrategyGuide()
